package simulator.playback;

public interface Vector3 {
    /**
     * Returns x coordinate.
     *
     * @return x coordinate.
     */
    float getX();

    /**
     * Returns y coordinate.
     *
     * @return y coordinate.
     */
    float getY();

    /**
     * Returns z coordinate.
     *
     * @return z coordinate.
     */
    float getZ();
}
